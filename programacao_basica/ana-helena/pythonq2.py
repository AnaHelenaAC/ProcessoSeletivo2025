## Uma variável é um espaço que é reservado na memória de um computador,
## o qual pode ser acessado posteriormente de acordo com a vontade do programado.
## De forma resumida, as variáveis podem ser explicadas como caixinhas que guardam 
## informações e que podem ser acessadas depois, cada caixa tem um endereço fixo, mas 
## o valor contido nela pode mudar com o decorrer do código. No Python elas não 
## precisam ser declaradas, é preciso apenas o nome da variável, seguida do símbolo "="
## e o valor que essa variável receberá.

## Criação de variável 
quantidade_asas = 2  ## Quantidade de asas de um aeromodelo (inteiro)

